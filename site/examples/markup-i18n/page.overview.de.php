<h1>Beispiel: Internationalisierung im Markup</h1>
<p>Dieses Beispiel beschreibt PHeasels einfachste Art und Weise der Internationalisierung: Markup-Internationalisierung.
Das heißt, dass Du eine Markup-Datei (z.B. ein Template, eine Seite oder ein Snippet) lokalisieren kannst, indem Du
es kopierst und seinen Inhalt übersetzt.</p>
<p>Wenn das in Deinen Ohren grundlegend falsch klingt, solltest Du nachlesen, <a href ="{ph:url pageid=markup-i18n-use-cases }">wann man diese Methode verwenden sollte</a>.</p>
<p>Wenn es in Deinen Ohren immer noch falsch klingt, ist die <a href ="{ph:url pageid=ini-i18n}">INI-Internationalisierung</a> vermutlich die bessere Wahl für Dich.</p>
<p>Weitere Funktionen, die in diesem Beispiel eingesetzt werden:</p>
<ul>
    <li>Snippets</li>
    <li>Templates</li>
</ul>

