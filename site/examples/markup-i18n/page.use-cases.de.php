<h1>Anwendungsfälle für Markup-Internationalisierung</h1>
<p>Markup-Internationalisierung ist der einfachste Weg, eine Markup-Datei zu übersetzen: Du speicherst die Datei unter
einem neuen Namen und übersetzt anschließend den Textinhalt. Natürlich duplizierst Du dabei die HTML-Tags in der Datei.</p>
<p>Diese Lösung ist ideal, wenn Deine Markup-Datei sehr viel Text enthält und wenig, semantisches HTML-Markup, z.B. Absätze,
Zeilenumbrüche, Links und vielleicht ein paar Grafiken oder ähnliches. Je mehr HTML Tags und je weniger Textinhalt Deine
Markup-Datei enthält, desto höher ist die Wahrscheinlichkeit, dass Du der <a href ="{ph:url pageid=ini-i18n}">INI-Internationalisierung</a>
den Vorzug gibst.</p>
<p> <a href ="{ph:url pageid=markup-i18n-usage }">Die Verwendung der Markup-Internationalisierung</a> ist ein Kinderspiel.</p>

