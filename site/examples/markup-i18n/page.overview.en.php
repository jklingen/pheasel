<h1>Markup internationalization example</h1>
<p>This example covers PHeasel's most straightforward way of internationalization, called markup internationalization.
Basically, this means you can localize a markup file (i.e. template, page or snippet) by creating a copy of it
and translating its content.</p>
<p>If this sounds just wrong to you, you should have a look at <a href ="{ph:url pageid=markup-i18n-use-cases}">when to use this method</a>.</p>
<p>If it still sounds wrong to you, you probably want to use <a href ="{ph:url pageid=ini-i18n}">INI internationalization</a> instead.</p>
<p>Other features used in this example:</p>
<ul>
    <li>Snippets</li>
    <li>Templates</li>
</ul>

